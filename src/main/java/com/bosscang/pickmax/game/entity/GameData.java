package com.bosscang.pickmax.game.entity;

public class GameData {
    private int n;
    private int index;
    private int value;

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
